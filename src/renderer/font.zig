/// Generate an R8 texture atlas from the glyph data.
/// Character `c` is placed at column `(c % 16)`, row `(c / 16)` in the atlas.
/// Each pixel is 0x00 (transparent) or 0xFF (opaque).
pub fn generateAtlas() [ATLAS_WIDTH * ATLAS_HEIGHT]u8 {
    var atlas: [ATLAS_WIDTH * ATLAS_HEIGHT]u8 = .{0} ** (ATLAS_WIDTH * ATLAS_HEIGHT);

    for (0..128) |c| {
        const glyph = glyphs[c];
        const col = c % ATLAS_COLS;
        const row = c / ATLAS_COLS;
        const base_x = col * GLYPH_WIDTH;
        const base_y = row * GLYPH_HEIGHT;

        for (0..GLYPH_HEIGHT) |gy| {
            const bits = glyph[gy];
            for (0..GLYPH_WIDTH) |gx| {
                const px = base_x + gx;
                const py = base_y + gy;
                const mask: u8 = @as(u8, 1) << @intCast(gx);
                atlas[py * ATLAS_WIDTH + px] = if (bits & mask != 0) 0xFF else 0x00;
            }
        }
    }

    return atlas;
}
